import "./styles/Work.css";
import WorkImage from "./WorkImage";
import gsap from "gsap";
import { ScrollTrigger } from "gsap/ScrollTrigger";
import { useGSAP } from "@gsap/react";

const projects = [
  { name: "Intelligent AI Chatbot", category: "NLP & LLM", tools: "Hugging Face, spaCy, LangChain", desc: "Created NLP chatbot for automated query responses. Result: ~88% intent recognition accuracy.", image: "/images/ai_chatbot.png" },
  { name: "DeepTrade-AI", category: "Stock Market Prediction System", tools: "LSTM, Random Forest, ML", desc: "Built ML models to predict stock price movements using historical financial data. Result: ~90% prediction accuracy.", image: "/images/stock_prediction.png" },
  { name: "Image Classification", category: "Deep Learning System", tools: "TensorFlow, Keras, CNN", desc: "Developed CNN-based image classifier using TensorFlow and Keras. Result: ~92% classification accuracy.", image: "/images/image_classification.png" }
];

gsap.registerPlugin(useGSAP);

const Work = () => {
  useGSAP(() => {
  let translateX: number = 0;

  function setTranslateX() {
    const box = document.getElementsByClassName("work-box");
    const rectLeft = document
      .querySelector(".work-container")!
      .getBoundingClientRect().left;
    const rect = box[0].getBoundingClientRect();
    const parentWidth = box[0].parentElement!.getBoundingClientRect().width;
    let padding: number =
      parseInt(window.getComputedStyle(box[0]).padding) / 2;
    let calculatedTranslateX = rect.width * box.length - (rectLeft + parentWidth) + padding;
    translateX = Math.max(0, calculatedTranslateX);
  }

  setTranslateX();

  let timeline = gsap.timeline({
    scrollTrigger: {
      trigger: ".work-section",
      start: "top top",
      end: `+=${translateX}`, // Use actual scroll width
      scrub: true,
      pin: true,
      id: "work",
    },
  });

  timeline.to(".work-flex", {
    x: -translateX,
    ease: "none",
  });

  // Clean up (optional, good practice)
  return () => {
    timeline.kill();
    ScrollTrigger.getById("work")?.kill();
  };
}, []);
  return (
    <div className="work-section" id="work">
      <div className="work-container section-container">
        <h2>
          My <span>Work</span>
        </h2>
        <div className="work-flex">
          {projects.map((project, index) => (
            <div className="work-box" key={index}>
              <div className="work-info">
                <div className="work-title">
                  <h3>0{index + 1}</h3>

                  <div>
                    <h4>{project.name}</h4>
                    <p>{project.category}</p>
                  </div>
                </div>
                <h4>Tools and features</h4>
                <p>{project.tools}</p>
              </div>
              <WorkImage image={project.image} alt={project.name} />
            </div>
          ))}
        </div>
      </div>
    </div>
  );
};

export default Work;
